REVIEW_RUBRIC = (
    "通用内容质量评审标准："
    "1. 只罗列资料、缺少分析判断时不通过；"
    "2. 核心结论主要依赖弱来源或无法核验案例时不通过，并指向 researcher；"
    "3. 预测性内容被写成已发生事实、综合案例写成公开案例时不通过；"
    "4. 报告缺少必要风险、限制或反例时不通过；"
    "5. 案例或应用章节只有连续段落、缺少成功案例/失败教训/横向或纵向分析等内部结构时不通过，并指向 writer；"
    "6. 结构只集中在前半篇，后半篇风险、证据边界、建议或展望章节重新变成连续段落时不通过，并指向 writer；"
    "7. 大量三级小节内容过薄、只有标题加短列表时不通过，并指向 writer；"
    "8. 各章节机械套用相同模板、表达空泛时不通过，并指向 writer；"
    "9. 只是可读性、标题、局部表达可优化时，通过并写入 suggestions，不要写入 specific_issues。"
)


def build_source_audit_text(source_summary: dict[str, int]) -> str:
    high_quality = source_summary.get("tier_1", 0) + source_summary.get("tier_2", 0)
    return (
        "来源质量审计："
        f"tier_1={source_summary.get('tier_1', 0)}，"
        f"tier_2={source_summary.get('tier_2', 0)}，"
        f"tier_3={source_summary.get('tier_3', 0)}，"
        f"unknown={source_summary.get('unknown', 0)}，"
        f"一手/准一手合计={high_quality}。"
    )
